    FROM TIME T JOIN PARTIDA P
    ON T.NOME = P.TIME1 OR T.NOME = P.TIME2
    JOIN JOGA J
    ON P.TIME1 = J.TIME1 AND P.TIME2 = J.TIME2
    GROUP BY T.NOME,EXTRACT(YEAR FROM P.DATA)
    HAVING COUNT(*) > 0;
*/

