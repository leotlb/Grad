SELECT J.CLASSICO, COUNT(*)
    FROM JOGA J JOIN PARTIDA P
    ON P.TIME1 = J.TIME1 AND P.TIME2 = J.TIME2
    WHERE EXTRACT(MONTH FROM P.DATA) <= 2
    GROUP BY J.CLASSICO;
*/

/*Q14
SELECT EXTRACT(MONTH FROM P.DATA) AS MES, COUNT(*) AS QTDE_DE_CLASSICOS
    FROM JOGA J JOIN PARTIDA P
    ON P.TIME1 = J.TIME1 AND P.TIME2 = J.TIME2
    WHERE J.CLASSICO = 'S' AND EXTRACT(YEAR FROM P.DATA) = 2018
    GROUP BY EXTRACT(MONTH FROM P.DATA)
    HAVING COUNT(*) > 1;
*/

